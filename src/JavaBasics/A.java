package JavaBasics;

public class A {

	public A() {
		System.out.println("Parent Class Constructor");
	}
	public A(int i) {
		System.out.println("Parent Class Constructor for value of i= "+i);
	}
	public A(int a, int b) {
		System.out.println("Parent Class Constructor for value of a= "+a);
		System.out.println("Parent Class Constructor for value of b= "+b);
	}
}
